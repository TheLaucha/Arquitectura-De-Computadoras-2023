ORG 3000h
MUL: MOV BX, SP
  ADD BX, 6
  MOV AX, [BX] ; Num_1
  SUB BX, 2
  MOV CX, [BX] ; Num_2
  MOV DX, 0
  CMP AX, 0
  JZ FIN
LOOP: CMP CX, 0
  JZ FIN
  ADD DX, AX
  DEC CX
  JMP LOOP
FIN: SUB BX, 2
  MOV AX, [BX]
  MOV BX, AX
  MOV [BX], DX
  RET

