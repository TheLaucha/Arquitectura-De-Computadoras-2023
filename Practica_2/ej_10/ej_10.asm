EOI EQU 20h
IMR EQU 21h
INT0 EQU 24h
ID_F10 EQU 10

; SELECCIONAR ID 10 PARA EL F10
ORG 40
IP_F10 DW RUT_F10

ORG 3000H
RUT_F10: PUSH AX
  INC DX
  MOV AL, EOI
  OUT EOI, AL
  POP AX
  IRET
	
ORG 2000H
  CLI
    MOV AL, 0FEh
    OUT IMR, AL
    MOV AL, ID_F10
    OUT INT0, AL
    MOV DX, 0    ; DX = Contador
  STI
LOOP: JMP LOOP
  INT 0
END