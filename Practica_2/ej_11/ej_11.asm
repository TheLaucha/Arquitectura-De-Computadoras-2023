EOI EQU 20h
IMR EQU 21h
INT0 EQU 24h
ID_F10 EQU 10

ORG 40
IP_F10 DW RUT_F10

ORG 1000h
  A DB "A"
  Z DB "Z"
  SELEC DB ?

ORG 3000H
RUT_F10: PUSH AX
  MOV SELEC, DL
  MOV BX, OFFSET SELEC
  MOV AL, 1
  INT 7
  MOV AL, EOI
  OUT EOI, AL
  POP AX
  IRET
	
ORG 2000H
  CLI
    MOV AL, 0FEh
    OUT IMR, AL
    MOV AL, ID_F10
    OUT INT0, AL
    MOV DL, A
  STI
LOOP: CMP DL, Z
  JNZ INCREMENTO
  MOV DL, A
  JMP FIN
INCREMENTO: INC DL
FIN: JMP LOOP
  INT 0
END