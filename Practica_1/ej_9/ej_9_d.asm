; VARIABLES
ORG 1000h
  NUM DB 94h
  CANT DB 06h

; AH Es el registro que se va a rotar
ORG 3000h
ROTARIZQ: MOV AH, [BX] 
  ADD AH, AH
  ADC AH, 0
  MOV [BX], AH
RET

ROTARDER_N: MOV DH, 8
  SUB DH, DL
  MOV DL, DH
LOOP: CMP DL, 00h
  JZ FIN
  CALL ROTARIZQ
  DEC DL
  JMP LOOP
FIN: RET
  

ORG 2000h
  MOV DL, CANT
  MOV BX, offset NUM
  CALL ROTARDER_N
HLT
END

